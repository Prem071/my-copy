
def arithFun(a,b,ops="add"):



    if ops == "add":
        print(a+b)
    elif ops == "sub":
        print(a-b)
    elif ops == "mul":
        print(a*b)
    elif ops == "div":
        print(a/b)
    else: 
        print("Invalid Input!")


arithFun(a=2,b=3, ops="mul")


